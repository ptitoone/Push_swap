long int	ft_atol(const char *str)
{
	long int	num;
	int			neg;
	int			i;

	num = 0;
	neg = 1;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-')
		neg = -1;
	if (str[i] != '-' && str[i] != '+')
		i--;
	while (str[++i] >= '0' && str[i] <= '9')
		num = (num * 10) + ((str[i] - '0') * neg);
	return (num);
}
